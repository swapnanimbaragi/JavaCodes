package Patterns.Loops;

public class Multiplication {
    public static void main(String... args)
    {
        
        int n=10;
         Scanner scan=new Scanner(System.in);
        int num=scan.nextInt(); 
        for(int i=1;i<=n;i++)
        {
            
            {
                System.out.println(i*num);
            }
        }
    }
   
   
}
    

